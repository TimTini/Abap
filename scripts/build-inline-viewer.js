"use strict";

const fs = require("fs");
const path = require("path");
const { spawnSync } = require("child_process");

const repoRoot = path.resolve(__dirname, "..");
const inputPath = path.join(repoRoot, "viewer", "index.html");
const outputPath = path.join(repoRoot, "viewer", "index.inline.html");
const runtimeBuilder = path.join(repoRoot, "scripts", "build-runtime-bundles.js");
const ATTR_RE = /([a-z_:][-a-z0-9_:.]*)\s*=\s*(?:"([^"]*)"|'([^']*)'|([^\s"'<>=`]+))/gi;
const LINK_TAG_RE = /<link\b[^>]*>/gis;
const SCRIPT_BLOCK_RE = /<script\b[^>]*>[\s\S]*?<\/script>/gi;
const SCRIPT_OPEN_RE = /<script\b[^>]*>/i;

function readText(filePath) {
  return fs.readFileSync(filePath, "utf8");
}

function writeText(filePath, text) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, text, "utf8");
}

function parseAttrs(openTag) {
  const attrs = {};
  for (const match of openTag.matchAll(ATTR_RE)) {
    const key = String(match[1] || "").trim().toLowerCase();
    const val = match[2] ?? match[3] ?? match[4] ?? "";
    if (key) attrs[key] = val;
  }
  return attrs;
}

function isRemoteUrl(url) {
  const value = String(url || "").trim().toLowerCase();
  return value.startsWith("http://") || value.startsWith("https://") || value.startsWith("//");
}

function escapeInlineScript(js) {
  return js.replace(/<\/script>/gi, "<\\/script>");
}

function inlineStylesheetLinks(html, baseDir, warnings) {
  let replaced = 0;
  const next = html.replace(LINK_TAG_RE, (tag) => {
    const attrs = parseAttrs(tag);
    if ((attrs.rel || "").trim().toLowerCase() !== "stylesheet" || !attrs.href) return tag;
    if (isRemoteUrl(attrs.href)) {
      warnings.push(`Skip remote stylesheet: ${attrs.href}`);
      return tag;
    }
    const cssPath = path.resolve(baseDir, attrs.href);
    if (!fs.existsSync(cssPath)) {
      warnings.push(`Missing stylesheet: ${attrs.href}`);
      return tag;
    }
    replaced += 1;
    return ["<style>", `/* inlined from: ${attrs.href} */`, readText(cssPath).trimEnd(), "</style>"].join("\n");
  });
  return { html: next, replaced };
}

function inlineScriptSrc(html, baseDir, warnings) {
  let replaced = 0;
  const next = html.replace(SCRIPT_BLOCK_RE, (block) => {
    const openMatch = block.match(SCRIPT_OPEN_RE);
    if (!openMatch) return block;
    const attrs = parseAttrs(openMatch[0]);
    const src = String(attrs.src || "").trim();
    if (!src || isRemoteUrl(src)) return block;
    const scriptPath = path.resolve(baseDir, src);
    if (!fs.existsSync(scriptPath)) {
      warnings.push(`Missing script: ${src}`);
      return block;
    }
    replaced += 1;
    return ["<script>", `// inlined from: ${src}`, escapeInlineScript(readText(scriptPath)).trimEnd(), "</script>"].join("\n");
  });
  return { html: next, replaced };
}

if (!fs.existsSync(inputPath)) process.exit(2);
if (fs.existsSync(runtimeBuilder)) {
  const result = spawnSync(process.execPath, [runtimeBuilder], { cwd: repoRoot, stdio: "inherit" });
  if (result.status !== 0) process.exit(result.status || 1);
}
const baseDir = path.dirname(path.resolve(inputPath));
const warnings = [];
let html = readText(inputPath);
const css = inlineStylesheetLinks(html, baseDir, warnings);
html = css.html;
const js = inlineScriptSrc(html, baseDir, warnings);
html = js.html;
const banner = [
  "<!--",
  "  AUTO-GENERATED FILE",
  `  Source: viewer/index.html`,
  "  Generator: scripts/build-inline-viewer.js",
  "  Do not edit this file directly. Regenerate instead.",
  "-->",
  ""
].join("\n");
writeText(outputPath, banner + html);
console.log(`Generated: ${outputPath} (inlined ${js.replaced} scripts, ${css.replaced} stylesheets)`);
